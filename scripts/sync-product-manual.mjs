import { access, copyFile, mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const siteRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const productRoot = process.env.SYNC_COORDINATOR_REPO
  ? path.resolve(process.env.SYNC_COORDINATOR_REPO)
  : path.resolve(siteRoot, "..", "SyncCoordinator");
const japaneseManualPath = path.join(productRoot, "docs", "user-guide.md");
const englishManualPath = path.join(productRoot, "docs", "user-guide.en.md");
const generatedDirectory = path.join(siteRoot, "app", "generated");
const generatedModule = path.join(generatedDirectory, "product-manual.ts");
const publicManualRoot = path.join(siteRoot, "public", "product-manual");

async function exists(value) {
  try {
    await access(value);
    return true;
  } catch {
    return false;
  }
}

if (!(await exists(japaneseManualPath)) || !(await exists(englishManualPath))) {
  if (await exists(generatedModule)) {
    console.warn(`One or more manual sources were not found under ${path.join(productRoot, "docs")}; using the synchronized snapshot.`);
    process.exit(0);
  }

  throw new Error(`Manual sources were not found under ${path.join(productRoot, "docs")}, and no synchronized snapshot exists.`);
}

const [japaneseMarkdown, englishMarkdown] = await Promise.all([
  readFile(japaneseManualPath, "utf8"),
  readFile(englishManualPath, "utf8"),
]);
const imageReferences = [...`${japaneseMarkdown}\n${englishMarkdown}`.matchAll(/!\[[^\]]*\]\((images\/pages\/[^)]+)\)/g)]
  .map((match) => match[1])
  .filter((value, index, values) => values.indexOf(value) === index);

await mkdir(generatedDirectory, { recursive: true });
await writeFile(
  generatedModule,
  [
    "// Generated by scripts/sync-product-manual.mjs. Do not edit directly.",
    `export const productManualMarkdown = ${JSON.stringify(japaneseMarkdown)};`,
    `export const productManualEnglishMarkdown = ${JSON.stringify(englishMarkdown)};`,
    "",
  ].join("\n"),
  "utf8",
);

for (const reference of imageReferences) {
  const normalized = path.posix.normalize(reference);
  if (!normalized.startsWith("images/pages/") || normalized.includes("..")) {
    throw new Error(`Unsupported manual image path: ${reference}`);
  }

  const relativeSegments = normalized.split("/");
  const sourcePath = path.join(productRoot, "docs", ...relativeSegments);
  if (!(await exists(sourcePath))) {
    throw new Error(`Manual image was not found: ${sourcePath}`);
  }

  const destinationPath = path.join(publicManualRoot, ...relativeSegments);
  await mkdir(path.dirname(destinationPath), { recursive: true });
  await copyFile(sourcePath, destinationPath);
}

console.log(`Synchronized 2 product manuals and ${imageReferences.length} images.`);
